def render_m1_summary(
    enriched_df: pd.DataFrame,
    well_id: str,
    *,
    show_geometry_and_survey: bool = True,
    show_editable_history: bool = True,
) -> None:
    st.header("Módulo 1 | Pozo e historia de producción")

    col1, col2, col3, col4 = st.columns(4)

    with col1:
        st.metric("Registros", f"{len(enriched_df):,}")

    with col2:
        if "date" in enriched_df.columns and enriched_df["date"].notna().any():
            st.metric("Fecha inicial", enriched_df["date"].min().date().isoformat())
        else:
            st.metric("Fecha inicial", "N/D")

    with col3:
        if "date" in enriched_df.columns and enriched_df["date"].notna().any():
            st.metric("Fecha final", enriched_df["date"].max().date().isoformat())
        else:
            st.metric("Fecha final", "N/D")

    with col4:
        if "pwf_source" in enriched_df.columns:
            measured_count = int((enriched_df["pwf_source"] == "measured").sum())
            estimated_count = int((enriched_df["pwf_source"] == "estimated").sum())
            manual_count = int((enriched_df["pwf_source"] == "manual").sum())
            st.metric(
                "Pwf med / est / man",
                f"{measured_count} / {estimated_count} / {manual_count}",
            )
        else:
            st.metric("Pwf med / est / man", "N/D")

    render_m1_production_plot(enriched_df)

    render_time_series(
        enriched_df,
        title="Presiones de cabeza y fondo",
        columns=M1_PRESSURE_COLUMNS,
        y_label="Presiones en psia.",
    )

    if "pwf_source" in enriched_df.columns:
        st.subheader("Distribución de fuente Pwf")
        source_counts = (
            enriched_df["pwf_source"]
            .fillna("unknown")
            .astype(str)
            .value_counts()
            .rename_axis("pwf_source")
            .reset_index(name="count")
        )
        st.dataframe(source_counts, use_container_width=True, hide_index=True)

    if show_geometry_and_survey:
        render_m1_geometry_and_survey_panel(well_id)

    if show_editable_history:
        render_m1_editable_history_panel(enriched_df, well_id)

    st.subheader("Tabla M1-M2 enriquecida")
    priority_columns = [
        column
        for column in (
            "well_id",
            "date",
            "qo_stb_d",
            "qg_mscf_d",
            "qw_stb_d",
            "whp_psia",
            "t_wh_f",
            "pwf_measured_psia",
            "pwf_estimated_psia",
            "pwf_manual_psia",
            "pwf_used_psia",
            "pwf_source",
            "tvd_perf_ft",
            "tubing_id_in",
            "length_ft",
        )
        if column in enriched_df.columns
    ]

    if priority_columns:
        st.dataframe(
            enriched_df[priority_columns],
            use_container_width=True,
            hide_index=True,
        )
    else:
        st.dataframe(enriched_df, use_container_width=True, hide_index=True)


