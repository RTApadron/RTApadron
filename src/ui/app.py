def save_uploaded_file(uploaded_file: Any, target_name: str) -> Path | None:
    """Persist an uploaded Streamlit file and return its local path."""
    if uploaded_file is None:
        return None

    target_path = UI_INPUT_DIR / target_name
    target_path.write_bytes(uploaded_file.getbuffer())
    return target_path


def get_active_edited_history_path() -> Path | None:
    """Return edited history path stored in session, if it exists."""
    raw_path = st.session_state.get(SESSION_EDITED_HISTORY_PATH)
    if not raw_path:
        return None

    path = Path(str(raw_path))
    if not path.exists():
        return None

    return path


def read_json(path: Path) -> dict[str, Any]:
    """Read a JSON object from disk."""
    with path.open("r", encoding="utf-8") as file:
        data = json.load(file)

    if not isinstance(data, dict):
        msg = f"El archivo JSON no contiene un objeto raíz válido: {path}"
        raise ValueError(msg)

    return data


def read_csv_safe(path: Path) -> pd.DataFrame | None:
    """Read CSV safely and normalize date column when available."""
    if not path.exists():
        return None

    try:
        df = pd.read_csv(path)
    except Exception as exc:  # noqa: BLE001
        st.error(f"No fue posible leer `{path.name}`: {exc}")
        return None

    if "date" in df.columns:
        df["date"] = pd.to_datetime(df["date"], errors="coerce")
        df = df.sort_values("date")

    return df


def as_float(value: Any, default: float = 0.0) -> float:
    """Convert a value to float safely."""
    try:
        if value is None:
            return default
        return float(value)
    except (TypeError, ValueError):
        return default


def existing_columns(df: pd.DataFrame, columns: tuple[str, ...]) -> list[str]:
    """Return columns that exist in a dataframe."""
    return [column for column in columns if column in df.columns]


def build_full_workflow_command(
    *,
    well_id: str,
    fit_from_date: date | None,
    fit_to_date: date | None,
    exclude_first_n: int,
    forecast_days: int,
    abandonment_rate: float,
    forecast_start_rate_mode: str,
    forecast_start_rate: float | None,
    history_csv: Path | None,
    pvt_config_json: Path | None,
) -> list[str]:
    """Build the existing CLI command for the full M1-M2-M3 workflow."""
    if forecast_start_rate_mode not in FORECAST_START_RATE_MODES:
        msg = f"Modo de tasa inicial de forecast no soportado: {forecast_start_rate_mode}"
        raise ValueError(msg)

    cmd = [
        sys.executable,
        "-m",
        "src.pipeline.run_full_workflow",
        "--well-id",
        well_id,
        "--exclude-first-n",
        str(exclude_first_n),
        "--forecast-days",
        str(forecast_days),
        "--abandonment-rate",
        str(abandonment_rate),
        "--forecast-start-rate-mode",
        forecast_start_rate_mode,
    ]

    if fit_from_date is not None:
        cmd.extend(["--fit-from-date", fit_from_date.isoformat()])

    if fit_to_date is not None:
        cmd.extend(["--fit-to-date", fit_to_date.isoformat()])

    if forecast_start_rate_mode == "manual":
        if forecast_start_rate is None or forecast_start_rate <= 0:
            msg = (
                "Cuando forecast_start_rate_mode='manual', "
                "forecast_start_rate debe ser mayor que cero."
            )
            raise ValueError(msg)

        cmd.extend(["--forecast-start-rate", str(forecast_start_rate)])

    if history_csv is not None:
        cmd.extend(["--history-csv", str(history_csv)])

    if pvt_config_json is not None:
        cmd.extend(["--pvt-config-json", str(pvt_config_json)])

    return cmd


def run_command(cmd: list[str]) -> subprocess.CompletedProcess[str]:
    """Run the workflow command from the project root."""
    return subprocess.run(
        cmd,
        cwd=PROJECT_ROOT,
        text=True,
        capture_output=True,
        check=False,
    )


def show_command(cmd: list[str]) -> None:
    """Render command in a readable shell-like form."""
    st.code(" ".join(cmd), language="bash")


