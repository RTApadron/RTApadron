def render_time_series(
    df: pd.DataFrame,
    *,
    title: str,
    columns: tuple[str, ...],
    y_label: str,
) -> None:
    """Render a Streamlit time-series chart for selected existing columns."""
    st.subheader(title)

    if "date" not in df.columns:
        st.info("No existe columna `date` para graficar serie de tiempo.")
        return

    selected_columns = existing_columns(df, columns)
    if not selected_columns:
        st.info(f"No hay columnas disponibles para graficar: {list(columns)}")
        return

    chart_df = df[["date", *selected_columns]].dropna(subset=["date"])
    if chart_df.empty:
        st.info("No hay datos válidos para graficar.")
        return

    chart_df = chart_df.set_index("date")
    st.line_chart(chart_df, y=selected_columns, height=360)
    st.caption(y_label)


def render_m1_production_plot(enriched_df: pd.DataFrame) -> None:
    """Render production rates with requested colors and secondary gas axis."""
    st.subheader("Producción por fase")

    if go is None or make_subplots is None:
        st.warning("Plotly no está disponible. No se puede graficar con eje secundario.")
        return

    if "date" not in enriched_df.columns:
        st.info("No existe columna `date` para graficar producción.")
        return

    available = existing_columns(enriched_df, M1_RATE_COLUMNS)
    if not available:
        st.info("No hay columnas de caudal disponibles para graficar.")
        return

    plot_df = enriched_df[["date", *available]].copy()
    plot_df["date"] = pd.to_datetime(plot_df["date"], errors="coerce")
    for column in available:
        plot_df[column] = pd.to_numeric(plot_df[column], errors="coerce")
    plot_df = plot_df.dropna(subset=["date"]).sort_values("date")

    if plot_df.empty:
        st.info("No hay datos válidos de producción para graficar.")
        return

    fig = make_subplots(specs=[[{"secondary_y": True}]])

    if "qo_stb_d" in plot_df.columns:
        fig.add_trace(
            go.Scatter(
                x=plot_df["date"],
                y=plot_df["qo_stb_d"],
                mode="markers+lines",
                name="Qo [STB/d]",
                line={"color": "green"},
                marker={"color": "green"},
            ),
            secondary_y=False,
        )

    if "qw_stb_d" in plot_df.columns:
        fig.add_trace(
            go.Scatter(
                x=plot_df["date"],
                y=plot_df["qw_stb_d"],
                mode="markers+lines",
                name="Qw [STB/d]",
                line={"color": "blue"},
                marker={"color": "blue"},
            ),
            secondary_y=False,
        )

    if "qg_mscf_d" in plot_df.columns:
        fig.add_trace(
            go.Scatter(
                x=plot_df["date"],
                y=plot_df["qg_mscf_d"],
                mode="markers+lines",
                name="Qg [MSCF/d]",
                line={"color": "red"},
                marker={"color": "red"},
            ),
            secondary_y=True,
        )

    fig.update_layout(
        title="Qo, Qw y Qg vs fecha",
        xaxis_title="Fecha",
        height=460,
        margin={"l": 20, "r": 20, "t": 60, "b": 20},
        legend={"orientation": "h"},
    )
    fig.update_yaxes(title_text="Qo / Qw [STB/d]", secondary_y=False)
    fig.update_yaxes(title_text="Qg [MSCF/d]", secondary_y=True)

    st.plotly_chart(fig, use_container_width=True)


def extract_plotly_selection_points(event: Any) -> list[dict[str, Any]]:
    """Extract selected points from a Streamlit Plotly selection event."""
    if isinstance(event, dict):
        selection = event.get("selection", {})
    else:
        selection = getattr(event, "selection", {})

    if isinstance(selection, dict):
        points = selection.get("points", [])
    else:
        points = getattr(selection, "points", [])

    if points is None:
        return []

    return list(points)


def parse_plotly_selected_dates(points: list[dict[str, Any]]) -> tuple[date, date] | None:
    """Extract min/max dates from selected Plotly points."""
    raw_x_values = [point.get("x") for point in points if point.get("x") is not None]
    if not raw_x_values:
        return None

    parsed_dates = pd.to_datetime(raw_x_values, errors="coerce")
    parsed_dates = parsed_dates[~pd.isna(parsed_dates)]

    if len(parsed_dates) == 0:
        return None

    start_date = parsed_dates.min().date()
    end_date = parsed_dates.max().date()

    if start_date > end_date:
        start_date, end_date = end_date, start_date

    return start_date, end_date


def parse_forecast_anchor_point(
    points: list[dict[str, Any]],
) -> tuple[date, float] | None:
    """Return the latest selected point as forecast anchor."""
    parsed_points: list[tuple[pd.Timestamp, float]] = []

    for point in points:
        raw_date = point.get("x")
        raw_rate = point.get("y")

        parsed_date = pd.to_datetime(raw_date, errors="coerce")
        rate = as_float(raw_rate, default=-1.0)

        if pd.isna(parsed_date) or rate <= 0:
            continue

        parsed_points.append((parsed_date, rate))

    if not parsed_points:
        return None

    anchor_timestamp, anchor_rate = max(parsed_points, key=lambda item: item[0])
    return anchor_timestamp.date(), anchor_rate


def update_fit_window_from_selection(start_date: date, end_date: date) -> None:
    """Store selected fit window and rerun before updating sidebar widgets."""
    current_start = st.session_state.get(SESSION_FIT_FROM_DATE)
    current_end = st.session_state.get(SESSION_FIT_TO_DATE)

    if current_start == start_date and current_end == end_date:
        return

    st.session_state[SESSION_PENDING_FIT_WINDOW] = {
        "start_date": start_date.isoformat(),
        "end_date": end_date.isoformat(),
    }

    st.rerun()


def update_forecast_anchor_from_selection(
    anchor_date: date,
    anchor_rate_stb_d: float,
) -> None:
    """Store selected forecast anchor and rerun before updating sidebar widgets."""
    current_mode = st.session_state.get(SESSION_FORECAST_START_RATE_MODE)
    current_rate = as_float(st.session_state.get(SESSION_FORECAST_START_RATE))

    if current_mode == "manual" and abs(current_rate - anchor_rate_stb_d) < 1e-9:
        return

    st.session_state[SESSION_PENDING_FORECAST_ANCHOR] = {
        "date": anchor_date.isoformat(),
        "rate_stb_d": float(anchor_rate_stb_d),
    }

    st.rerun()


def build_qo_plot_dataframe(enriched_df: pd.DataFrame) -> pd.DataFrame | None:
    """Build a clean dataframe with date and qo for interactive DCA plots."""
    required_columns = {"date", "qo_stb_d"}
    missing_columns = required_columns.difference(enriched_df.columns)
    if missing_columns:
        st.info(
            "No es posible construir la gráfica interactiva. "
            f"Faltan columnas: {sorted(missing_columns)}"
        )
        return None

    plot_df = enriched_df[["date", "qo_stb_d"]].copy()
    plot_df["date"] = pd.to_datetime(plot_df["date"], errors="coerce")
    plot_df["qo_stb_d"] = pd.to_numeric(plot_df["qo_stb_d"], errors="coerce")
    plot_df = plot_df.dropna(subset=["date", "qo_stb_d"]).sort_values("date")

    if plot_df.empty:
        st.info("No hay datos válidos de qo_stb_d para construir la gráfica.")
        return None

    return plot_df


def render_interactive_dca_fit_window_selector(enriched_df: pd.DataFrame) -> None:
    """Render interactive DCA fit-window selector using Plotly selection events."""
    st.subheader("Selección interactiva de ventana de ajuste DCA")

    if go is None:
        st.warning(
            "Plotly no está instalado. Agrega `plotly>=5.22` a requirements.txt "
            "y ejecuta `pip install plotly`."
        )
        return

    plot_df = build_qo_plot_dataframe(enriched_df)
    if plot_df is None:
        return

    fit_from = st.session_state.get(SESSION_FIT_FROM_DATE)
    fit_to = st.session_state.get(SESSION_FIT_TO_DATE)

    fig = go.Figure()

    fig.add_trace(
        go.Scatter(
            x=plot_df["date"],
            y=plot_df["qo_stb_d"],
            mode="markers+lines",
            name="qo_stb_d",
            marker={"size": 7},
            customdata=plot_df["date"].dt.strftime("%Y-%m-%d"),
            hovertemplate=(
                "Fecha: %{customdata}<br>"
                "qo: %{y:,.2f} STB/d"
                "<extra></extra>"
            ),
        )
    )

    if isinstance(fit_from, date) and isinstance(fit_to, date):
        fig.add_vrect(
            x0=fit_from.isoformat(),
            x1=fit_to.isoformat(),
            fillcolor="LightSkyBlue",
            opacity=0.22,
            layer="below",
            line_width=0,
            annotation_text="Ventana actual",
            annotation_position="top left",
        )

    fig.update_layout(
        title="Arrastra una caja sobre los puntos que quieres usar para el ajuste",
        xaxis_title="Fecha",
        yaxis_title="Tasa de aceite, qo [STB/d]",
        dragmode="select",
        height=520,
        margin={"l": 20, "r": 20, "t": 60, "b": 20},
        legend={"orientation": "h"},
    )

    event = st.plotly_chart(
        fig,
        use_container_width=True,
        on_select="rerun",
        selection_mode=("box", "lasso"),
        key="dca_fit_window_selector",
        config={
            "displaylogo": False,
            "modeBarButtonsToAdd": ["select2d", "lasso2d"],
        },
    )

    st.caption(
        "Uso: selecciona puntos con click-and-drag. "
        "La app tomará la fecha mínima y máxima seleccionada como "
        "`fit_from_date` y `fit_to_date`."
    )

    points = extract_plotly_selection_points(event)

    if not points:
        st.info(
            "No hay selección activa. Puedes seguir usando las fechas manuales "
            "en la barra lateral."
        )
        return

    selected_window = parse_plotly_selected_dates(points)
    if selected_window is None:
        st.warning("No fue posible interpretar fechas en la selección.")
        return

    start_date, end_date = selected_window

    st.success(
        "Ventana seleccionada: "
        f"{start_date.isoformat()} → {end_date.isoformat()}. "
        "Actualizando controles laterales..."
    )
    update_fit_window_from_selection(start_date, end_date)


