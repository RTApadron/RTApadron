def compute_module_status(well_id: str, output_dir: Path) -> dict[str, str]:
    """Return status for each module: 'ok' | 'warning' | 'missing'.

    Used by the sidebar navigation to show 🟢 / 🟡 / 🔴 indicators.
    """
    safe = well_id.strip()
    status: dict[str, str] = {}

    # M1 — historia enriquecida + QC
    enriched = output_dir / f"{safe}_history_enriched.csv"
    if enriched.exists():
        qc_json = output_dir / f"{safe}_dca_qc_report.json"
        has_warnings = False
        if qc_json.exists():
            try:
                import json as _json
                with open(qc_json, encoding="utf-8") as _f:
                    _qc = _json.load(_f)
                if isinstance(_qc.get("warnings"), list) and _qc["warnings"]:
                    has_warnings = True
            except Exception:
                pass
        status["M1"] = "warning" if has_warnings else "ok"
    else:
        status["M1"] = "missing"

    # M2 — configuración PVT guardada explícitamente por el usuario desde M2.
    # pvt_config_ui.json solo existe si el usuario guardó desde el editor M2.
    # pvt_config_default.json es auto-generado por el pipeline y no cuenta como "configurado".
    pvt_cfg = UI_INPUT_DIR / f"{safe}_pvt_config_ui.json"
    if pvt_cfg.exists():
        status["M2"] = "ok"
    else:
        # Not yet configured by user — warn so it's visible but not blocking
        status["M2"] = "warning"

    # M3 — resultados DCA
    dca_fit = output_dir / f"{safe}_dca_fit_results.csv"
    dca_qc = output_dir / f"{safe}_dca_qc_report.json"
    if dca_fit.exists():
        has_dca_warnings = False
        if dca_qc.exists():
            try:
                import json as _json
                with open(dca_qc, encoding="utf-8") as _f:
                    _dqc = _json.load(_f)
                sims = _dqc.get("models_with_similar_rmse", [])
                if isinstance(sims, list) and len(sims) > 1:
                    has_dca_warnings = True
            except Exception:
                pass
        status["M3"] = "warning" if has_dca_warnings else "ok"
    else:
        status["M3"] = "missing"

    # M4 — diagnósticos RTA + match
    rta_diag = output_dir / f"{safe}_rta_diagnostics.csv"
    rta_match = output_dir / f"{safe}_rta_match_summary.json"
    if rta_match.exists():
        status["M4"] = "ok"
    elif rta_diag.exists():
        status["M4"] = "warning"
    else:
        status["M4"] = "missing"

    # M5 — al menos un módulo aguas arriba tiene datos
    upstream_ok = any(v in ("ok", "warning") for v in list(status.values()))
    status["M5"] = "ok" if upstream_ok else "missing"

    return status


def initialize_session_defaults() -> None:
    pending_window = st.session_state.pop(SESSION_PENDING_FIT_WINDOW, None)
    if pending_window is not None:
        try:
            st.session_state[SESSION_USE_FIT_FROM] = True
            st.session_state[SESSION_USE_FIT_TO] = True
            st.session_state[SESSION_FIT_FROM_DATE] = date.fromisoformat(
                pending_window["start_date"]
            )
            st.session_state[SESSION_FIT_TO_DATE] = date.fromisoformat(
                pending_window["end_date"]
            )
        except (KeyError, TypeError, ValueError):
            pass

    pending_anchor = st.session_state.pop(SESSION_PENDING_FORECAST_ANCHOR, None)
    if pending_anchor is not None:
        try:
            st.session_state[SESSION_FORECAST_START_RATE_MODE] = "manual"
            st.session_state[SESSION_FORECAST_START_RATE] = float(
                pending_anchor["rate_stb_d"]
            )
        except (KeyError, TypeError, ValueError):
            pass

    st.session_state.setdefault(SESSION_USE_FIT_FROM, True)
    st.session_state.setdefault(SESSION_USE_FIT_TO, True)
    st.session_state.setdefault(SESSION_FIT_FROM_DATE, date(2024, 7, 1))
    st.session_state.setdefault(SESSION_FIT_TO_DATE, date(2025, 11, 30))
    st.session_state.setdefault(
        SESSION_FORECAST_START_RATE_MODE,
        "last-window-rate",
    )
    st.session_state.setdefault(SESSION_FORECAST_START_RATE, 100.0)
    st.session_state.setdefault(
        SESSION_PWF_OVERRIDE_MODE,
        "prefer_measured_else_estimated",
    )
    st.session_state.setdefault(SESSION_ACTIVE_MODULE, "Inicio")


